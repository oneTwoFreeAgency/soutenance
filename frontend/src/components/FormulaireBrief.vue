              <h2>1/ QUI? QUOI?</h2>
              <h3>
                Qui vous êtes, quel est votre marché, quels sont vos produits
                et/ou services?
              </h3>
              <p>
                L'historique de votre marque, votre positionnement, détail sur
                le marché, les produits, la concurrence... Même si l’agence fera
                un travail de bench sur votre marque, nourrissez-nous un maximum
                sur votre ADN avec vos propres mots.
              </p>
              <TEXTAREA name="nom" rows="4" cols="55"></TEXTAREA>
              <h2>2/ COMMENT?</h2>
              <h3>Quel est le contexte?</h3>
              <p>
                Il résumera votre besoin et l’origine de votre démarche.
                Pourquoi avez-vous besoin de refondre votre site? Pourquoi
                re-positionner votre marque? Pourquoi avez-vous besoin de lancer
                une campagne?
              </p>
              <TEXTAREA name="contexte" rows="4" cols="55"></TEXTAREA>
              <h2>3/ POURQUOI?</h2>
              <h3>Quels sont vos objectifs?</h3>
              <p>
                Augmenter vos ventes? Générer du traffic? Augmenter votre
                notoriété? Améliorer votre image? Acquisition? Fidélisation? La
                stratégie proposée et les leviers qui en découleront ne seront
                pas les mêmes. Dans tous les cas il faut qu'ils soient précis et
                mesurables. Attention bien sûr à les prioriser.
              </p>
              <TEXTAREA name="objectifs" rows="4" cols="55"></TEXTAREA>
              <h2>4/ POUR QUI?</h2>
              <h3>Quelle est la cible?</h3>
              <p>
                A qui va-t-on s’adresser? Aux clients actuels? Aux prospects?
                Aux CSP+? Aux +35 ans? Quels sont les freins qui pourraient les
                empêcher d'acheter vos produits ou d'utiliser vos services?
              </p>
              <TEXTAREA name="cibles" rows="4" cols="55"></TEXTAREA>
              <h2>5/ MESSAGE</h2>
              <h3>
                Quel est le message que vous voulez envoyer à votre cible?
              </h3>
              <p>
                Cela peut-être un message mais aussi une image, un sentiment ou
                bien encore un ton à faire passer.
              </p>
              <TEXTAREA name="message" rows="4" cols="55"></TEXTAREA>
              <h2>6/ CONTRAINTES</h2>
              <h3>
                Rétroplanning du projet, contraintes organisationnelles, respect
                d’une charte existante, contraintes techniques…
              </h3>
              <p>
                Vos contraintes sont les nôtres, il ne faut les voir comme des
                obstacles à sauter, elles nous inspirent, elles donnent un
                cadre, parfois des directions des idées et même des solutions.
              </p>
              <TEXTAREA name="contraintes" rows="4" cols="55"></TEXTAREA>
              <h2>7/ ATTENTES</h2>
              <h3>Le périmètre précis de vos besoins</h3>
              <p>
                - S’il s’agit d’une campagne de communication : L’agence aura
                t-elle en charge le volet recommandation média? La négociation
                auprès des supports?
              </p>
              <p>
                - S’il s’agit d’un site : Attendez vous un travail UX / UI? Une
                intégration front en HTML, ou l’agence doit-elle aussi
                développer le back office?
              </p>
              <p>
                - Si vous envisagez une prestation de contenus social média :
                Sur quel support? Avec quelle densité de message? L’agence
                devra-t-elle gérer la publication et la modération?
              </p>
              <TEXTAREA name="besoins" rows="4" cols="55"></TEXTAREA>
              <h2>9/ BUDJET</h2>
              <h3>Un périmètre budgétaire</h3>
              <p>
                Une même demande peut être traitée bien différemment selon le
                budget. L’agence saura s’adapter à vos besoins si elle connaît
                votre enveloppe. Nous courrons tous à la frustration si les
                propositions sont plus ambitieuses que le budget. Si vous ne
                connaissez pas votre enveloppe définitive, n'hésitez pas à
                indiquer une fourchette de prix pour que l’agence soit en mesure
                de cadrer sa réflexion. Vous pouvez aussi ne pas vouloir ou
                pouvoir, pour de bonnes raisons, nous communiquer votre budjet
                et nous demander une reco et/ou une estimation.
              </p>
